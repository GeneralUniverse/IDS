DROP TABLE zamestnanec;
DROP TABLE zakaznik;
DROP TABLE zakaznik_s_radnym_uctem;

CREATE TABLE zamestnanec
(
    zamestnanec_ID
        INT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    jmeno
        VARCHAR(60) NOT NULL,
    datum_nastupu
        DATE NOT NULL,
    datum_vypovedi
        DATE NOT NULL,
    pozice
        VARCHAR(60)
);

CREATE TABLE zakaznik_s_radnym_uctem
(
    zakaznik_id_s_radnym_uctem_id
        INT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    adresa
        VARCHAR(1024) NOT NULL, -- zakaznik s radnym uctem se vyznacuje tim, ze o nem mame blizsi informace
    kategorie_zakaznika
        VARCHAR(16) NOT NULL CHECK (kategorie_zakaznika IN ('pravnicka osoba', 'fyzicka osoba'))
);

CREATE TABLE zakaznik
(
    zakaznik_id
        INT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    jmeno
        VARCHAR(60) NOT NULL,
    email
        NVARCHAR2(60),
    CHECK (REGEXP_LIKE(
            email, '^[a-z]+[a-z0-9\.]*@[a-z0-9\.-]+\.[a-z]{2,}$', 'i'
        )),
    telefonni_cislo
        VARCHAR(20) NOT NULL,
    login
        VARCHAR(18) NOT NULL,
    heslo
        VARCHAR(256) NOT NULL,
    zakaznik_s_radnym_uctem_fk
        INT DEFAULT NULL,
    CONSTRAINT zakaznik_s_radnym_uctem_fk
        FOREIGN KEY (zakaznik_s_radnym_uctem_fk) REFERENCES zakaznik_s_radnym_uctem (zakaznik_id_s_radnym_uctem_id)
            ON DELETE SET NULL
);

CREATE TABLE nemovitost
(
    nemovitost_ID
        INT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    typ_nemovitosti
        VARCHAR(60),
    plocha
        INT CHECK ( plocha >= 0 ),
    poloha
        VARCHAR(200),
    cena
        DECIMAL(12, 3) CHECK ( cena >= 0 ),
    blizsi_popis
        VARCHAR(2048),
    zamestnanec
        INT DEFAULT NULL,
    vlastnik
        INT DEFAULT NULL,
    zakaznik
        INT DEFAULT NULL,
    CONSTRAINT vlozil_do_systemu_fk
        FOREIGN KEY (zamestnanec) REFERENCES zamestnanec (zamestnanec_ID)
            ON DELETE SET NULL,
    CONSTRAINT vlastnik_fk
        FOREIGN KEY (vlastnik) REFERENCES zakaznik_s_radnym_uctem (zakaznik_id_s_radnym_uctem_id)
            ON DELETE SET NULL
);

CREATE TABLE kupni_smlouva
(
    smlouva_ID
        INT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    datum_uzavreni_smlouvy
        DATE DEFAULT NULL,
    stav
        VARCHAR(20) DEFAULT 'neuzavrena' CHECK ( stav IN ('uzavrena', 'neuzavrena')),
    obsah_smlouvy
        VARCHAR(4000) NOT NULL,
    zakaznik
        INT DEFAULT NULL,
    zamestnanec
        INT DEFAULT NULL,
    nemovitost
        INT DEFAULT NULL,
    CONSTRAINT figurujici_zakaznik_fk
        FOREIGN KEY (zakaznik) REFERENCES zakaznik_s_radnym_uctem (zakaznik_id_s_radnym_uctem_id)
            ON DELETE SET NULL,
    CONSTRAINT zadal_zamestnanec_fk
        FOREIGN KEY (zamestnanec) REFERENCES zamestnanec (zamestnanec_ID)
            ON DELETE SET NULL,
    CONSTRAINT figurujici_nemovitost_fk
        FOREIGN KEY (nemovitost) REFERENCES nemovitost (nemovitost_ID)
            ON DELETE SET NULL
);

CREATE TABLE zadost_o_prohlidku
(
    zadost_ID
        INT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    pozadovany_datum_a_cas_prohlidky
        DATE NOT NULL,
    datum_podani_zadosti
        TIMESTAMP,
    probehla
        VARCHAR(3) DEFAULT 'ne' CHECK (probehla IN ('ano', 'ne')),
    nemovitost
        INT DEFAULT NULL,
    zakaznik
        INT DEFAULT NULL,
    CONSTRAINT prohlidka_nemovitosti_fk
        FOREIGN KEY (nemovitost) REFERENCES nemovitost (nemovitost_ID)
            ON DELETE SET NULL,
    CONSTRAINT zadajici_zakaznik_fk
        FOREIGN KEY (zakaznik) REFERENCES zakaznik (zakaznik_id)
            ON DELETE SET NULL
);

CREATE TABLE nabidka_ke_koupi
(
    nabidka_ID
        INT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    vyse_nabidky
        DECIMAL(12, 3) NOT NULL CHECK ( vyse_nabidky >= 0 ),
    datum_podani_nabidky
        TIMESTAMP,
    nemovitost
        INT DEFAULT NULL,
    zakaznik
        INT DEFAULT NULL,
    CONSTRAINT zadana_nemovitost_fk
        FOREIGN KEY (nemovitost) REFERENCES nemovitost (nemovitost_ID)
            ON DELETE SET NULL,
    CONSTRAINT nabizejici_zakaznik_fk
        FOREIGN KEY (zakaznik) REFERENCES zakaznik (zakaznik_id)
            ON DELETE SET NULL
);

